def human_but_smaller(data: str):
    swap_dict = {
    	' and': '',
        ' hour': ' hr',
        ' minute': ' min',
        ' seconds': ' sec'
    }
    for key, value in swap_dict.items():
        data = data.replace(key, value)
    return data
